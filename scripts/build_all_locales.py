#!/usr/bin/env python3
"""Regenerate locale JSON files from keys in cs.json (cs is not overwritten)."""
from __future__ import annotations

import json
import pathlib

ROOT = pathlib.Path(__file__).resolve().parent.parent
CS_PATH = ROOT / "locales" / "cs.json"
OUT_DIR = ROOT / "locales"


def load_cs_ordered() -> tuple[list[str], set[str]]:
    cs = json.loads(CS_PATH.read_text(encoding="utf-8"))
    order = [k for k in cs if k != "_meta"]
    return order, set(order)


def write(code: str, label: str, strings: dict[str, str], key_order: list[str], keyset: set[str]) -> None:
    missing = keyset - set(strings)
    extra = set(strings) - keyset
    if missing:
        raise SystemExit(f"{code}: missing keys: {sorted(missing)[:10]}… ({len(missing)})")
    if extra:
        raise SystemExit(f"{code}: extra keys: {sorted(extra)[:10]}… ({len(extra)})")
    data: dict[str, object] = {"_meta": {"label": label}}
    for k in key_order:
        data[k] = strings[k]
    (OUT_DIR / f"{code}.json").write_text(
        json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
    )


